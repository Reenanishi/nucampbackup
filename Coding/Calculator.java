class Calculator{
    static int add(int a, int b){
        return a + b;
    }
    static double add( double a, double b){
        return a + b;

    }
    public static void main(String args[]){
        System.out.println(Calculator.add(123,17));
        System.out.println(Calculator.add(18.3,1.9));
    }

}


